def tokenize(text: str) -> set[str]:
    words = text.lower().split()
    tokens = set()
    for word in words:
        cleaned = "".join(c for c in word if c.isalnum())
        if cleaned and cleaned not in STOPWORDS and len(cleaned) > 2:
            tokens.add(cleaned)
    return tokens


def score_chunk(query_tokens: set[str], chunk: Chunk) -> int:
    chunk_tokens = tokenize(chunk.text)
    return len(query_tokens & chunk_tokens)


def retrieve_chunks(query: str, chunks: list[Chunk], top_k: int = 5) -> list[Chunk]:
    if not chunks:
        return []

    query_tokens = tokenize(query)
    if not query_tokens:
        return chunks[:top_k]

    scored = [(score_chunk(query_tokens, chunk), chunk) for chunk in chunks]
    scored.sort(key=lambda x: x[0], reverse=True)

    return [chunk for score, chunk in scored[:top_k] if score > 0]
